package main

import "fmt"

func main() {

	// short declaration operator :=
	// can only be used inside a function body
	// DECLARE a variable & ASSIGN value of a certain type
	// here x is an INTEGER
	a := 20
	fmt.Println(a)

	// ADDITION operator
	b := a + 23
	fmt.Println(b)

	// here c is a STRING
	c := "Addition done.."
	println(c)
}
